int ConvertMCXItoI(char str[SIZE]){
	int i = 0;
	int ans = 0;	//?????????????????????
	int coef = 1;	//?????°???
	for( i = 0; i < SIZE || str[i] != '\0'; i++ ){
		if( '2' <= str[i] && str[i] <= '9' ){
			coef = str[i] - '0';
		}else if( str[i] == 'm' ){
			ans += coef * 1000;
			coef = 1;
		}else if( str[i] == 'c' ){
			ans += coef * 100;
			coef = 1;
		}else if( str[i] == 'x' ){
			ans += coef * 10;
			coef = 1;
		}else if( str[i] == 'i' ){
			ans += coef;
		}
	}
	return ans;
}

