#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <math.h>

//============================================================================
#define succ(x) ((x)+1)
#define pred(x) ((x)-1)
//============================================================================
void enter()
{
    int i,x;

    for (i=0;; i++){
        scanf("%d", &x);
        if (!x)
            break;
        printf("Case %d: %d\n", succ(i), x);
    }
}
//============================================================================
int main(void)
{
    enter();
    return 0;
}
