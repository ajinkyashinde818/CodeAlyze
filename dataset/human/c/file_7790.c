#include <stdio.h>

 int main(void)
 {
     int times;              // N
     scanf("%d", &times);

     int value[times];       // K
     int headCount[times];   // P
     int i;

     // printf("???????????????????????°?????\????????????????????????");

     for (i = 0; i < times; i++)
     {
         // printf("???????????°??¨?????°?????\????????????????????????");
         scanf("%d %d", &value[i], &headCount[i]);
     }

     for (i = 0; i < times; i++)
     {
         if ((value[i] % headCount[i]) == 0)
         {
             printf("%d\n", headCount[i]);
         }
         else
         {
             printf("%d\n", value[i] % headCount[i]);
         }
     }

     return 0;
 }
