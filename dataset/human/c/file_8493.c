#include <stdio.h>
int main(void)
{
  int h,b[10][5],f[10][5]={},x,y,c,s,i,j,k,t;
  while(scanf("%d",&h)&&h){
    s=0;
    for(i=0;i<h;i++){
      for(j=0;j<5;j++)
	scanf("%d",&b[i][j]);
    }
    while(1){
      y=0;
      for(i=0;i<h;i++){
	c=2;
	x=0;
	for(j=0;j<3;j++){
	  if(b[i][j]&&b[i][j]==b[i][j+1]&&b[i][j+1]==b[i][j+2]){
	    x=b[i][j];
	    c++;
	    f[i][j]=1;
	    f[i][j+1]=1;
	    f[i][j+2]=1;
	    y=1;
	  }
	}
	s+=x*c;
	for(j=0;j<5;j++){
	  if(f[i][j])
	    b[i][j]=0;
	}
      }
      if(!y)
	break;
      for(i=0;i<h;i++){
	for(j=0;j<5;j++){
	  if(f[i][j]){
	    f[i][j]=0;
	    for(k=i;k>0;k--){
	      t=b[k][j];
	      b[k][j]=b[k-1][j];
	      b[k-1][j]=t;
	    }
	  }
	}
      }
    }
    printf("%d\n",s);
  }
  return 0;
}
