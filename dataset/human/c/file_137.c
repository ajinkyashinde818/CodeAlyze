#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
/********************************************************************************************************************************/
/* main *************************************************************************************************************************/
/********************************************************************************************************************************/
int DEBUG = 1;										/* デバッグプリント 提出時は0 */

int main()
{

	int		R,G,B,N;
	int		t,tt;
	int		r,g,b;
	int		ans = 0;

	scanf("%d %d %d %d", &R, &G, &B, &N);

	for (r=0; r*R<=N; r++) {
		t = r*R;
		for (g=0; t+g*G<=N; g++) {
			tt = t + g*G;
			if ((N-tt)%B == 0) {
				ans++;
			}
		}
	}
	printf("%d\n",ans);

	return 0;
}
