#include<stdio.h>
int main(void)
{
	int w,h,n,sa1,sa2,x[1000],y[1000],cnt,i,a;
	scanf("%d %d %d",&w,&h,&n);
	for(i=0;i<n;i++){
		scanf("%d",&x[i]);
		scanf("%d",&y[i]);
	}
	for(i=0;i<n-1;i++){
		sa1=x[i+1]-x[i];
		sa2=y[i+1]-y[i];
		if(sa1>0&&sa2>0){
			if(sa1<sa2){
				cnt+=sa1;
				x[i]=x[i]+sa1;
				y[i]=y[i]+sa1;
			}
			else{
				cnt=cnt+sa2;
				x[i]=x[i]+sa2;
				y[i]=y[i]+sa2;
			}
		}
		if(sa1<0&&sa2<0){
			sa1=-sa1;
			sa2=-sa2;
			if(sa1<sa2){
				cnt=cnt+sa1;
				x[i]=x[i]-sa1;
				y[i]=y[i]-sa1;
			}
			else{
				cnt=cnt+sa2;
				x[i]=x[i]-sa2;
				y[i]=y[i]-sa2;
			}
		}
		sa1=x[i+1]-x[i];
		sa2=y[i+1]-y[i];
		if(sa1<0){
			sa1=-sa1;
		}
		cnt=cnt+sa1;
		if(sa2<0){
			sa2=-sa2;
		}
		cnt=cnt+sa2;
	}
	printf("%d\n",cnt);
	return 0;
}
