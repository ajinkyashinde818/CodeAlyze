#include<stdio.h>

#define MAX 10000
#define MIN 1

int main()
{
	int a;
	char c;
	
	for(int i=0; i<MAX; i++)
	{
		if( scanf("%d%c", &a, &c)==2 )
		{
			if(a==0)
			{
				break;
			}
			else
			{
				printf("Case %d: %d\n", i+1, a);
			}
		}
		else
		{
			/* ERROR */
			break;
		}
	}
	
	return 0;
}
