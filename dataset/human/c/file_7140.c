#include <stdio.h>
int main(void)
{
    int i,j,H,W,quotient1,quotient2,remainder1,remainder2;
    while(1)
    {
        scanf("%d%d",&H,&W);
        quotient1=H/2;
        quotient2=W/2;
        remainder1=H%2;
        remainder2=W%2;
        if(quotient1>0)
        {
            for(i=0;i<quotient1;i++)
            {
                if(quotient2>0)
                {
                    for(j=0;j<quotient2;j++)
                    {
                        printf("#.");
                    }
                }
                if(remainder2==1)
                {
                    printf("#");
                }
                printf("\n");
                if(quotient2>0)
                {
                    for(j=0;j<quotient2;j++)
                    {
                        printf(".#");
                    }
                }
                if(remainder2==1)
                {
                    printf(".");
                }
                printf("\n");
            }
        }
        if(remainder1==1)
        {
            if(quotient2>0)
                {
                    for(j=0;j<quotient2;j++)
                    {
                        printf("#.");
                    }
                }
                if(remainder2==1)
                {
                    printf("#");
                }
                printf("\n");
        }
        if(H==0 && W==0)
        break;
        printf("\n");
    }
    return 0;
}
