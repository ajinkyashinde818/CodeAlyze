#include<stdio.h>
int main(){
int max=0,a[1010][1010],i,j,dp[1020][1020],min=1000000,n;
char k;
while(1){
for(i=0;i<1010;i++){
for(j=0;j<1010;j++){
dp[i][j]=0;
}
}
scanf("%d\n",&n);
if(n==0){
break;
}
for(i=1;i<=n;i++){
for(j=1;j<=n;j++){
scanf("%c",&k);
if(k=='*'){
a[i][j]=1;
}
else{
a[i][j]=0;
}
}
scanf("\n");
}
for(i=1;i<=n;i++){
for(j=1;j<=n;j++){
if(i==1 && j==1){
if(a[i][j]==0){
dp[i][j]=1;    
}    
}
else{
if(a[i][j]==1){
dp[i][j]=0;
}
else{
if(dp[i-1][j-1]<min){
min=dp[i-1][j-1];
}
if(dp[i-1][j]<min){
min=dp[i-1][j];
}
if(dp[i][j-1]<min){
min=dp[i][j-1];
}
dp[i][j]=min+1;
min=1000000;
}
}
}
}
for(i=1;i<=n;i++){
for(j=1;j<=n;j++){
if(dp[i][j]>max){
max=dp[i][j];
}
}
}
printf("%d\n",max);
max=0;
}
return 0;
}
