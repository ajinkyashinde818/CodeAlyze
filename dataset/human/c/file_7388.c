#include <stdio.h>

int main(int argc, char* argv[])
{
	int i;
	int num;
	int buf[10000];
	char temp[10];

	for (num = 0; 1; num++) {
		fgets(temp, sizeof(temp), stdin);
		sscanf(temp, "%d", &buf[num]);
		if (buf[num] == 0) break;
	}
	for (i = 0; i < num; i++) {
		printf("Case %d: %d\n", i+1, buf[i]);
	}
	
	return 0;
}
