struct circle{
	int x, y, r;
	circle(){}
	circle(int _x, int _y, int _r){
		x = _x, y = _y, r = _r;
	}
	ld dist(circle a){
		return max((ld)0.0, sqrtl((x-a.x)*(x-a.x) + (y-a.y)*(y-a.y))-r-a.r);
	}
};

void solve(){
	int x1, y1, x2, y2;
	cin >> x1 >> y1 >> x2 >> y2;
	int n;
	cin >> n;
	vector<circle> kek(n+2);
	kek[0].x = x1;
	kek[0].y = y1;
	kek[0].r = 0;
	kek[n+1].x = x2;
	kek[n+1].y = y2;
	kek[n+1].r = 0;
	for(int i=1; i<=n; i++){
		int x, y, r;
		cin >> x >> y >> r;
		kek[i].x = x;
		kek[i].y = y;
		kek[i].r = r;
	}
	vector<ld> dist(n+2, INFLL);
	dist[0] = 0;
	pqueue<pii> q;
	vb used(n+2, false);
	q.push(make_pair(0, 0));
	while(!q.empty()){
		int v = q.top().second;
		q.pop();
		if(used[v])
			continue;
		used[v] = true;
		for(int i=0; i<=n+1; i++){
			if(dist[i] - (dist[v] + kek[v].dist(kek[i])) > eps){
				dist[i] = dist[v] + kek[v].dist(kek[i]);
				q.push(make_pair(-dist[i], i));
			}
		}
	}
	cout << dist[n+1] << endl;
}
 
