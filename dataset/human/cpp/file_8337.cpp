#include <bits/stdc++.h>
using namespace std;
using ll = long long ;

const string YES = "Yes";
const string NO = "No";

void solve(long long N, long long M, std::vector<std::string> A, std::vector<std::string> B){
    bool isGood = false;
    for(ll i=0; i<= N-M; i++){
        for(ll j=0; j<= N-M; j++){
            bool subGood = true;
            for(ll k=0; k<M; k++){
                if(A[i+k].substr(j, M) != B[k]) {
                    subGood = false;
                    break;
                }
            }
            if(subGood){
                cout << YES << endl;
                return;
            }
        }
    }
    cout << NO << endl;
    return;
}

