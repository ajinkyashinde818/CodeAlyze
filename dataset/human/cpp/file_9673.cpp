const string YES = "Yes";
const string NO = "No";

void solve(long long N, long long M, std::vector<std::string> A, std::vector<std::string> B) {
    for (long long i = 0; i < N; i++) {
        for (long long j = 0; j < N; j++) {
            long long cnt = 0;
            for (long long k = 0; k < M; k++) {
                for (long long l = 0; l < M; l++) {
                    if (i + k >= N || j + l >= N) {
                        continue;
                    }
                    if (B[k][l] == A[i + k][j + l]) {
                        cnt++;
                    }
                }
            }
            if (cnt == M * M) {
                cout << "Yes" << endl;
                return;
            }
        }
    }
    cout << "No" << endl;
}

