#include <bits/stdc++.h>
using namespace std;
using ll = long long;

const string YES = "Yes";
const string NO = "No";

void solve(long long N, long long M, std::vector<std::string> A, std::vector<std::string> B)
{
    for (ll i = 0; i < N - M + 1; ++i) {
        for (ll j = 0; j < N - M + 1; ++j) {
            bool match = true;
            for (ll k = 0; k < (ll)B.size(); ++k) {
                if (!equal(begin(B[k]), end(B[k]), begin(A[i + k]) + j)) {
                    match = false;
                    break;
                }
            }
            if (match) {
                cout << YES << endl;
                return;
            }
        }
    }
    cout << NO << endl;
}

