#include <bits/stdc++.h>
using namespace std;
using ll = long long;
using P = pair<double, ll>;

const ll INF = 1e18;

struct Barrier {
    complex<double> z;
    ll r;
};

void solve(long long x_s, long long y_s, long long x_t, long long y_t, long long N, std::vector<long long> x, std::vector<long long> y, std::vector<long long> r)
{
    vector<Barrier> barriers;

    // スタートとゴールも半径0のバリアとして追加する
    barriers.push_back({ { (double)x_s, (double)y_s }, 0 });
    for (ll i = 0; i < N; i++) {
        barriers.push_back({ { (double)x[i], (double)y[i] }, r[i] });
    }
    barriers.push_back({ { (double)x_t, (double)y_t }, 0 });

    priority_queue<P, vector<P>, greater<P>> que;
    vector<double> costs(N + 2, INF);

    que.emplace(0, 0);
    costs[0] = 0;

    while (!que.empty()) {
        auto a = que.top();
        que.pop();
        auto cost = a.first;
        auto v = a.second;

        if (cost > costs[v]) {
            continue;
        }

        for (ll i = 0; i <= N + 1; i++) {
            if (i == v) {
                continue;
            }
            auto nc = max(0.0, abs(barriers[v].z - barriers[i].z) - barriers[v].r - barriers[i].r);
            if (costs[i] > cost + nc) {
                costs[i] = cost + nc;
                que.emplace(cost + nc, i);
            }
        }
    }

    cout << setprecision(15) << costs[N + 1] << endl;
}

